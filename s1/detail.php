				<h3>Чем укроп может помочь</h3>
				<p>Укроп может помочь не только восполнить недостаток витаминов и микроэлементов. Эта трава
					благотворно влияет на сердечно-сосудистую систему, обладая сосудорасширяющим действием и
					укрепляя сосудистую стенку; стимулирует выведение желчи; обеспечивает послабление кишечника
					и
					устраняет метеоризм; обладает мочегонным эффектом; поддерживает иммунную функцию организма
					благодаря своим антиоксидантным свойствам. Укроп используют также в косметологии. На основе
					укропа изготавливают шампуни, лосьоны, гели для тела и маски для лица.</p>
