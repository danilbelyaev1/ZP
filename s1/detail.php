				<h3>Витамины в укропе</h3>
				<p>Укроп богат витаминами С, РР, Р, Е, В1, В2, В6, бета-каротином, фолиевой кислотой (витамин
					В9);
					минералами – калием, марганцем, медью, цинком, кальцием, фосфором и железом; биологически
					активными веществами – хлорофиллом, лютеином, виолексантином, а также флавоноидами, такими
					как
					кемпферол и викенин, и монотерпенами – карвоном, лимоненом и анетофураном, азотистыми
					соединениями.</p>

				<p>В 100 г свежего укропа содержится 43 калории, 3 г белка, 1 г жира, 61 мг натрия, 7 г
					углеводов
					(из них 2 г – в виде пищевых волокон).</p>

				<p>При изучении масла укропа было установлено, что монотерпены, входящие в его состав, обладают
					химиопротекторным действием. Эти соединения индуцируют детоксифицирующий фермент
					глутатион-S-трансферазу, помогая в борьбе с окислительным стрессом, нейтрализуя канцерогены,
					с
					которыми большинство из нас сталкиваются ежедневно (сигаретный дым, выхлопные газы
					автомобилей и
					прочее).</p>
