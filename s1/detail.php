				<h3>Чем полезен укроп</h3>
				<p>Практически любой суп вкуснее с укропом, обычная яичница становится изысканной, а банальное
					картофельное пюре – пикантным. Рыба, птица, соленья, маринады, салаты ... Список блюд, в
					которые
					можно добавить укроп, бесконечен.</p>
				<br>
				<br>
				<p>Укроп (лат. Anethum) – однолетнее травянистое растение семейства сельдерейные, единственный
					вид
					которого – укроп пахучий или укроп огородный. Укроп в диком виде произрастает в Малой Азии,
					Северной
					Африке, Иране, Гималаях. Как культивируемое и сорное растение укроп встречается повсеместно,
					на
					всех
					континентах.</p>
				<br>
				<br>
				<p>Укроп – одна из древнейших трав, используемых человеком. Упоминание о нем есть в египетских
					рукописях, где его применяли в качестве лекарства. Напиток из семян укропа использовали при
					головной
					боли, при коликах у детей, расстройствах желудка, нервном перенапряжении, для улучшения сна.
					Семена
					укропа жевали для освежения дыхания, облегчения менструальной боли, увеличения притока
					молока у
					кормящих женщин.
					Диоскорид (древнегреческий военный врач и фармаколог, один из основателей ботаники и
					фармакогнозии)
					рекомендовал укроп для лечения глазных болезней, а Гален (древнеримский врач и философ) –
					для
					облегчения болезненного мочеиспускания. Укроп использовали не только как лекарственное,
					пряное,
					но и
					как декоративное растение.</p>
